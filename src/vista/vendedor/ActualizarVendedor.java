    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed
        boolean entradaValida = validarEntradas();
        if(entradaValida){

        Vendedor vendedor = new Vendedor();
        vendedor.setRut_vendedor(Integer.parseInt(txt_rut.getText()));
        vendedor.setNombre(txt_nombre.getText());
        vendedor.setApellido(txt_apellido.getText());
        vendedor.setContacto(Integer.parseInt(txt_contacto.getText()));
        vendedor.actualizarDatos();
        this.dispose();    
        }
        else{
            JOptionPane.showMessageDialog(this, "Existen campos vac√≠os");
        }
    }//GEN-LAST:event_btn_actualizarActionPerformed
    
    /**
     * metodo que valida que no existan entradas vacias en el formulario
     * @return  boolean con validacion 
     */
    private boolean validarEntradas() {
    boolean entradasValidas=true;
        if(txt_rut.getText().isEmpty() || txt_nombre.getText().isEmpty() || txt_apellido.getText().isEmpty() || txt_contacto.getText().isEmpty() ){
            entradasValidas = false;
        
    }  
    return entradasValidas;
    }
