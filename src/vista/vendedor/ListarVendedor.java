    /**
     * metodo necesario para listar y agregar en tabla los vendedors extraidos de la BD
     */
    private void listarVendedor() {
        tabla = new DefaultTableModel();
        Vendedor vendedor = new Vendedor();
        ArrayList<Vendedor> listaVendedor = new ArrayList();
        listaVendedor = vendedor.listarVendedor();
        tabla.addColumn("Rut");
        tabla.addColumn("Nombre");
        tabla.addColumn("Apellido");
        tabla.addColumn("Contacto");
        tabla.addColumn("Fecha de Registro");
        tabla.addColumn("Usuario Registro");
        tabla.setRowCount(listaVendedor.size());
        
        int i= 0;
        for(Vendedor t : listaVendedor){
           tabla.setValueAt(t.getRut_vendedor(), i, 0);
           tabla.setValueAt(t.getNombre(), i, 1);
           tabla.setValueAt(t.getApellido(), i, 2);
           tabla.setValueAt(t.getContacto(), i, 3);
           i++;
        }
        this.tbl_vendedor.setModel(tabla);
    }

