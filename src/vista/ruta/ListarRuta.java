    /**
     * metodo nesario para llenar tabla con los datos extraidos de la BD
     */
    private void listarRuta(){
       tabla = new DefaultTableModel();
       Ruta ruta = new Ruta();
       ArrayList<Ruta> listaRuta = new ArrayList();
       listaRuta = ruta.listarRuta();
       tabla.addColumn("ID Ruta");
       tabla.addColumn("Dia de Ruta");
       tabla.addColumn("Rut Vendedor");
       tabla.setRowCount(listaRuta.size());
       int i = 0;
       for(Ruta rut: listaRuta){
         tabla.setValueAt(rut.getId_ruta(), i, 0);
         tabla.setValueAt(rut.getDia(), i, 1);
         tabla.setValueAt(rut.getRut_vendedor(), i, 2);
         i++;
       }
     tbl_ruta.setModel(tabla);
    }

