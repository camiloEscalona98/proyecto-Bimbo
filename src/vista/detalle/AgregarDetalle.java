    private void cargarCombos() {

        listaGenerica = new ListaGenerica();
        
        //Producto
        mapProducto = listaGenerica.generarValoresProducto();
        for (String producto : mapProducto.keySet()) {
            cmb_producto.addItem(producto);
        }
        //TipoDevolucion
        mapTipoDevolucion = listaGenerica.generarValoresTipoDevolucion();
        for (String tipo : mapTipoDevolucion.keySet()) {
            cmb_tipo.addItem(tipo);
        }
    }
    
            

    

