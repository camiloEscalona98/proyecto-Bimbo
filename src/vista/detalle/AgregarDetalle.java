    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int idProducto = mapProducto.get(cmb_producto.getSelectedItem().toString());
        int idTipo = mapTipoDevolucion.get(cmb_tipo.getSelectedItem().toString());
        int cantidad = Integer.parseInt(txt_cantidad.getText());
        int valor = Integer.parseInt(txt_valor.getText());
        
        Detalle detalle = new Detalle();
        detalle.setId_producto(idProducto);
        detalle.setId_tipo_devolucion(idTipo);
        detalle.setCantidad(cantidad);
        detalle.setValor_total(valor);
        detalle.agregarDatos();
     
        
    }//GEN-LAST:event_jButton1ActionPerformed

