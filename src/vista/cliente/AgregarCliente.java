    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      int rut = Integer.parseInt(txt_rut.getText());
      String nombre = txt_nombre.getText();
      String direccion = txt_direccion.getText();
      int id_giro = mapGiro.get(cmb_giro.getSelectedItem().toString());
      int id_ruta = mapRuta.get(cmb_ruta.getSelectedItem().toString());
      int id_ciudad = mapCiudad.get(cmb_ciudad.getSelectedItem().toString());
      
      
      Cliente cliente = new Cliente();
      cliente.setRut_cliente(rut);
      cliente.setNombre(nombre);
      cliente.setDireccion(direccion);
      cliente.setId_giro(id_giro);
      cliente.setId_ruta(id_ruta);
      cliente.setId_ciudad(id_ciudad);
      cliente.agregarDatos();
      
      
    }//GEN-LAST:event_jButton1ActionPerformed

