    /**
     * metodo necesario para cargar ComboBox en el Jframe con datos de la BD
     */
    private void cargarCombo() {
       listaGenerica = new ListaGenerica();
       //ciudad
       mapCiudad = listaGenerica.generarValoresCiudad();
       for (String ciudad : mapCiudad.keySet()){
       cmb_ciudad.addItem(ciudad);
       }
       //ruta
       mapRuta = listaGenerica.generarValoresRuta();
       for (String dia : mapRuta.keySet()){
       cmb_ruta.addItem(dia);
       }
       //giro
       mapGiro = listaGenerica.generarValoresGiro();
       for (String giro : mapGiro.keySet()){
       cmb_giro.addItem(giro);
       }
       
    }
