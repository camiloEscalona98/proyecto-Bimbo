    /**
     * metodo encargado de listar datos en la tabla creada en el Jframe
     */
    private void listarCliente(){
        tabla = new DefaultTableModel();
        Cliente cliente = new Cliente();
        ArrayList<Cliente> listaCliente = new ArrayList();
        listaCliente = cliente.listarCliente();
        tabla.addColumn("Rut Cliente");
        tabla.addColumn("Nombre");
        tabla.addColumn("direccion");
        tabla.addColumn("Giro");
        tabla.addColumn("Ciudad");
        tabla.addColumn("Ruta");
        tabla.setRowCount(listaCliente.size());
        int i=0;
        for(Cliente cl : listaCliente){
          tabla.setValueAt(cl.getRut_cliente(), i, 0);
          tabla.setValueAt(cl.getNombre(), i, 1);
          tabla.setValueAt(cl.getDireccion(), i, 2);
          tabla.setValueAt(cl.getId_giro(), i, 3);
          tabla.setValueAt(cl.getId_ciudad(), i, 4);
          tabla.setValueAt(cl.getId_ruta(), i, 5);
          i++;
        }
        tbl_cliente.setModel(tabla);
        
        
    }

